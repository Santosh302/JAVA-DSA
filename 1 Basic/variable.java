        age = 25;
        System.out.println(age); //print 25

        short sample_num = 10000;
        System.out.println(sample_num);

        long sample_long_num= 100000000000l;
        System.out.println(sample_long_num);

         //decimal numbers
         double dec_num_1 =51.4;
         System.out.println(dec_num_1);

         //decimal numbers
         float dec_num_2 = 51.4f;
